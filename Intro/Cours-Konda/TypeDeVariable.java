public class TypeDeVariable {

        public static void main(String[] args) {

            int i = 10;
            float f = 10.0f*4.3f;
            double d = 10.0*4.3;
            String s = "ABC";
            boolean b = true;
            char c = '$';

            System.out.println("entier  :"+i);
            System.out.println("Float (decimal) :"+f);
            System.out.println("Double (décimal plus précis) :"+d);
            System.out.println("Chaîne de caractère (décimal plus précis) :"+s);
            System.out.println("Boolean (Binaire) :"+b);
            System.out.println("Char (Caractère) :"+c);               





        }
    
}
